const now = new Date();
const d = (daysAgo: number, hoursAgo = 0, minutesAgo = 0): string => {
  const date = new Date(
    now.getTime() -
      daysAgo * 86400000 -
      hoursAgo * 3600000 -
      minutesAgo * 60000,
  );
  return date.toISOString();
};

